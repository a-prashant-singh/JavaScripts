
    // Example array of numbers
const numbers = [1, 2, 3, 4, 5];

// Using indexOf() to find the index of a specific value in the array
const indexThree = numbers.indexOf(3);
const indexTen = numbers.indexOf(10);

console.log(indexThree); // Output: 2 (index of value 3)
console.log(indexTen);   // Output: -1 (value 10 is not found in the array)

