
    // Example string
const str = 'Hello, World!';

// Using charAt() to get the character at index 0
const firstChar = str.charAt(0);

console.log(firstChar); // Output: 'H'

// Using charAt() to get the character at index 7
const eighthChar = str.charAt(7);

console.log(eighthChar); // Output: 'W'

// Using charAt() to get the character at an index beyond the string length
const beyondLengthChar = str.charAt(20);

console.log(beyondLengthChar); // Output: '' (empty string)

